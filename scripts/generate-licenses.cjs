/**
 * Generate THIRD_PARTY_LICENSES.md from node_modules using license-checker.
 * Run: npm run licenses
 */

const checker = require("license-checker");
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const outFile = path.join(root, "THIRD_PARTY_LICENSES.md");

checker.init({ start: root, production: true }, (err, json) => {
  if (err) {
    console.error("license-checker failed:", err);
    process.exit(1);
  }

  const entries = Object.entries(json).filter(([k]) => !k.startsWith("wshell@"));

  let md = "# Third-Party Licenses\n\n";
  md += "Auto-generated by `npm run licenses` (license-checker).\n\n";

  // Summary table
  md += "## Summary\n\n";
  md += "| Package | License | Repository |\n";
  md += "|---------|---------|------------|\n";
  for (const [pkg, info] of entries.sort((a, b) => a[0].localeCompare(b[0]))) {
    const repo = info.repository || "-";
    md += `| ${pkg} | ${info.licenses} | ${repo} |\n`;
  }

  // Full license texts
  md += "\n---\n\n## Full License Texts\n\n";
  for (const [pkg, info] of entries.sort((a, b) => a[0].localeCompare(b[0]))) {
    md += `### ${pkg} (${info.licenses})\n\n`;
    if (info.licenseFile && fs.existsSync(info.licenseFile)) {
      const text = fs.readFileSync(info.licenseFile, "utf8");
      md += "```\n" + text.trim() + "\n```\n\n";
    } else {
      md += "_No license file found._\n\n";
    }
  }

  fs.writeFileSync(outFile, md);
  console.log(`Generated THIRD_PARTY_LICENSES.md with ${entries.length} packages`);
});
