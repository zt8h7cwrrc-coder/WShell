/**
 * Generate THIRD_PARTY_LICENSES/ from node_modules using license-checker.
 * Run: npm run licenses
 */

const checker = require("license-checker");
const fs = require("fs");
const path = require("path");

const outDir = path.join(__dirname, "..", "THIRD_PARTY_LICENSES");

checker.init({ start: path.join(__dirname, ".."), production: true }, (err, json) => {
  if (err) {
    console.error("license-checker failed:", err);
    process.exit(1);
  }

  fs.mkdirSync(outDir, { recursive: true });

  const summary = [];

  for (const [pkg, info] of Object.entries(json)) {
    if (pkg.startsWith("wshell@")) continue;

    const license = info.licenses;
    const repo = info.repository || "";
    const licenseFile = info.licenseFile;

    if (licenseFile && fs.existsSync(licenseFile)) {
      const baseName = pkg.replace(/@[^@]+$/, "").replace(/[^a-zA-Z0-9._-]/g, "_");
      const ext = path.extname(licenseFile) || ".txt";
      const outName = baseName + ext;
      fs.copyFileSync(licenseFile, path.join(outDir, outName));
      summary.push({ name: pkg, license, file: outName, repo });
    } else {
      summary.push({ name: pkg, license, file: null, repo });
    }
  }

  // Group by license
  const byLicense = {};
  for (const s of summary) {
    if (!byLicense[s.license]) byLicense[s.license] = [];
    byLicense[s.license].push(s);
  }

  // Generate README
  let readme = "# Third-Party Licenses\n\n";
  readme += "Auto-generated by `npm run licenses` (license-checker).\n\n";
  readme += "## Summary\n\n";
  readme += "| Package | License |\n|---------|--------|\n";
  for (const s of summary.sort((a, b) => a.name.localeCompare(b.name))) {
    readme += `| ${s.name} | ${s.license} |\n`;
  }
  readme += "\n";

  for (const [license, pkgs] of Object.entries(byLicense).sort()) {
    readme += `## ${license}\n\n`;
    for (const p of pkgs.sort((a, b) => a.name.localeCompare(b.name))) {
      readme += `### ${p.name}\n\n`;
      if (p.repo) readme += `Repository: ${p.repo}\n\n`;
      if (p.file) readme += `License text: [${p.file}](${p.file})\n\n`;
    }
  }

  fs.writeFileSync(path.join(outDir, "README.md"), readme);
  console.log(`Generated ${summary.length} license files in THIRD_PARTY_LICENSES/`);
});
